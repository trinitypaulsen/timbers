package com.trinity;

public class User {

  private int id;
  String name;

  public void setId (int i) {
      this.id = i;
  }

  public void setName (String n) {
      this.name = n;
  }

  public int getId() {
      return this.id;
  }

  public String getName() {
      return this.name;
  }

}
